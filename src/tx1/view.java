    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
       String content = inputMessage.getText().trim();

        if (content.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nội dung không được để trống.");
            return;
        }

        Message newMessage = new Message(content, new Date(), 1, 1,1); 
        Message insertedMessage = messageDAO.them(newMessage);

        if (insertedMessage != null) {
            inputMessage.setText(""); 
            JOptionPane.showMessageDialog(this, "Gửi tin nhắn thành công!");
            loadMessagesToTable();
        } else {
            JOptionPane.showMessageDialog(this, "Gửi tin nhắn thất bại.");
        }
    }//GEN-LAST:event_btnSendActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if(selectedMessage == null){
            JOptionPane.showMessageDialog(this, "Vui lòng chọn tin nhắn cần cập nhật");
        }else{
            boolean result = messageDAO.xoa(selectedMessage.getId());
            if(result){
                inputMessage.setText(""); 
                JOptionPane.showMessageDialog(this, "Xóa tin nhắn thành công!");
                selectedMessage = null;
                loadMessagesToTable();
            }else {
                JOptionPane.showMessageDialog(this, "Xóa tin nhắn thất bại.");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed
    
    
