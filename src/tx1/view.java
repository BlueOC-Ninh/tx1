    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if(selectedMessage == null){
            JOptionPane.showMessageDialog(this, "Vui lòng chọn tin nhắn cần cập nhật");
        }else{
            String content = inputMessage.getText();
            Message newMessage = selectedMessage;
            newMessage.setContent(content);
            boolean result = messageDAO.capNhat(selectedMessage);
            
            if(result){
                JOptionPane.showMessageDialog(this, "Cập nhật nhắn thành công");
                loadMessagesToTable();
                selectedMessage = null;
                inputMessage.setText("");
            }else{
                 JOptionPane.showMessageDialog(this, "Cập nhật nhắn thành công");
            }
        }
       
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnTKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTKActionPerformed
        String startStr = inputFrom.getText().trim();
        String endStr = inputTo.getText().trim();

        if (!isValidDate(startStr)) {
            JOptionPane.showMessageDialog(this, "Ngày bắt đầu không hợp lệ. Định dạng đúng: yyyy-MM-dd");
            return;
        }

        if (!isValidDate(endStr)) {
            JOptionPane.showMessageDialog(this, "Ngày kết thúc không hợp lệ. Định dạng đúng: yyyy-MM-dd");
            return;
        }

        try {
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            Date startDate = sdf.parse(startStr);
            Date endDate = sdf.parse(endStr);

            MessageDAO dao = new MessageDAO(); 
            MessageStatistics stats = dao.thongKe(startDate, endDate);


            String result =
                    "Tổng số tin nhắn: " + stats.getTotalMessages() + "\n" +
                    "Người gửi nhiều nhất (user_id): " + stats.getTopSenderUserId() + "\n" +
                    "Phòng nhiều tin nhắn nhất (room_id): " + stats.getTopRoomId();

            JOptionPane.showMessageDialog(this, result);

        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this, "Không thể phân tích ngày. Hãy kiểm tra lại định dạng.");
        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(this, "Đã xảy ra lỗi khi thống kê: " + e.getMessage());
        }    
    }//GEN-LAST:event_btnTKActionPerformed
  
    private void loadMessagesToTable() {
    DefaultTableModel model = (DefaultTableModel) tableMessages.getModel();
    model.setRowCount(0); 

    List<Message> danhSach = messageDAO.layTatCa();

    for (Message msg : danhSach) {
        Object[] row = new Object[] {
            msg.getId(),
            msg.getContent(),
            msg.getUserId(),
            msg.getSendDate(),
            msg.getRoomId(),
            msg.getMessageTypeId()
        };
        model.addRow(row);
    }
}
    
    private void searchMesloadsages(String keyword) {
    DefaultTableModel model = (DefaultTableModel) tableMessages.getModel();
    model.setRowCount(0); 

    List<Message> danhSach = messageDAO.timKiem(keyword);

    for (Message msg : danhSach) {
        Object[] row = new Object[] {
            msg.getId(),
            msg.getContent(),
            msg.getUserId(),
            msg.getSendDate(),
            msg.getRoomId(),
            msg.getMessageTypeId()
                
        };
        model.addRow(row);
    }
}
    
    public boolean isValidDate(String input) {
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        sdf.setLenient(false); 

        try {
            sdf.parse(input);
            return true;
        } catch (ParseException e) {
            return false;
        }
}

    
